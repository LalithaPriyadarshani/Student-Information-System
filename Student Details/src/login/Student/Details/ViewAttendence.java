    private void fill_combo() {

        try {

            Class.forName("com.mysql.jdbc.Driver");

            String constr = "jdbc:mysql://localhost:3306/student_information";

            Connection dbcon = DriverManager.getConnection(constr, "root", "123");
            Statement stmt = dbcon.createStatement();

            String query = "select course from attendence where  reg_no='" + txtID.getText() + "'  ";

            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                combo_course.addItem(rs.getString("course"));

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

