    private void combo_courseItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combo_courseItemStateChanged
        try {

            //String name=Txt_name.getText();
            String id = txtID.getText();


            String cours = combo_course.getSelectedItem().toString();

            Class.forName("com.mysql.jdbc.Driver");

            String constr = "jdbc:mysql://localhost:3306/student_information";

            Connection dbcon = DriverManager.getConnection(constr, "root", "123");
            Statement stmt = dbcon.createStatement();

            String query = "select precentage from attendence where course='" + cours + "' and reg_no='" + id + "' ";

            ResultSet rs = stmt.executeQuery(query);

            if (rs.next()) {

                Txt_attend.setText(String.valueOf(rs.getDouble("precentage")));


            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }//GEN-LAST:event_combo_courseItemStateChanged

